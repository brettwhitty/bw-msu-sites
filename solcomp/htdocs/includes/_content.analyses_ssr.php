      <h2>Solanaceae Simple Sequence Repeats (SSR)</h2>

        <p>Using an in silico approach simple sequence repeats (SSR) were identified using sequences from all Solanaceae transcript sequence assemblies within the <a href='http://www.plantgdb.org/prj/ESTCluster/index.php'>PlantGDB PUTs</a> database. SSRs are defined as repeats of at least 10 mononucleotides, 6 dinucleotides, 5 trinucleotides, 5 tetranucleotides, 5 pentanucleotides, and 5 hexanucleotides. We also designed primers to amplify these SSRs using Primer3.</p>
           
