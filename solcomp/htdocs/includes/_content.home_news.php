  <h1>News</h1>
    <a name="news_060509" id="news_060509">
  <h3>June 5, 2009</h3>
  </a>
  <ul>
  <li>Site resources have been updated.<li>
  <li>Integrated overview page for EST assemblies with search portal allowing search by functional annotation.</li>
  <li>Updates to our model genome browsers including improved annotation of features and OrthoMCL cluster membership information.</li>
  <li>Improved SNP viewer showing multiple sequence alignments of EST assemblies.</li>
  <li>Genome browser has been updated to Gbrowse 1.69, which has an improved interface.</li>
  </ul>  
  <a name="news_020309" id="news_020309">
  <h3>February 3, 2009</h3>
  </a>
  <ul>
  <li>Release 3 of the Solanaceae Comparative Genomics project resources.</li>
  <li>The current set of 1,810 Solanaceae BACs from GenBank was reannotated using an updated version of the MAKER annotation pipeline. A total of 29,234 predicted gene models were annotated and are available for BLAST searches, viewing in Gbrowse and for download on our FTP site.</li>
  <li>Sequence data from the Tobacco Genome Initiative's public release is now available for BLAST searching.
  <li>The Tobacco Genome Initiative 454- and Sanger-sequenced sheared BAC assemblies were annotated using the MAKER pipeline. Results are available for BLAST searching and download: Sanger: 979 assemblies / 594 models, 454: 7,978 assemblies / 508 models. Annotation of methyl-filtered assemblies will be available soon.
  <li>The Solanaceae vs. Arabdidopsis genome browser has been updated with improved exonerate alignments.</li>
  <li>The site's top menu has been reorganized for easier use. The species dropdown is now organized by genus, and summary pages are viewable for more species. A more complete FTP site map is available on our downloads page.</li>
  </ul>
  
  <a name="news_010909" id="news_010909">
  <h3>January 9, 2009</h3>
  </a>
  <ul>
  <li>Many site resources have been updated.</li>
  <li>Changes to our SNP-calling pipeline have resulted in a significant increase in identified putative SNPs.</li>
  <li>Annotation text keyword searching is now available for SSRs.</li>
  <li>An additional 278 BACs have been added to the Solanaceae genome browser.</li>
