<h3>Solanaceae Repeat Database</h3>

<p>The Solanaceae genomes consist of repetitive DNA sequence intermixed with coding sequence. We have <a href="http://plantrepeats.plantbiology.msu.edu/about.html">created</a> the Solanaceae Repeat Database to assist in the compilation and identification of repeat sequences in the Solanaceae genomes. All of the repetitive sequences in the database are <a href="http://plantrepeats.plantbiology.msu.edu/about.html#codes">coded</a> for the convenience of future analysis.
        The Solanaceae Repeat Database is a part of the <a href="http://plantrepeats.plantbiology.msu.edu">Plant Repeat Databases</a>.
        </p>

