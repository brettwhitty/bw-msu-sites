  <h1>Comparative genomics resources for the Solanaceae: Tools and resources for cross-species translational genomics</h1>
  <h3> Background</h3>
  <p>The large and morphologically diverse family <a href="http://en.wikipedia.org/wiki/Solanaceae">Solanaceae</a> includes a number of agriculturally important species, notably Tomato (<em>Solanum lycopersicum</em>), Potato (<em>Solanum tuberosum</em>), Tobacco (<em>Nicotiana tabacum</em>), Eggplant (<em>Solanum melongena</em>) and Chili Pepper (<em>Capsicum annuum</em>), and ranks third in the list of most economically significant plant taxa (ranking first among vegetable crops).</p>
  <p>The Solanaceae Comparative Genomics Resource will provide a robust, rich, and integrated resource allowing broad and deep data-mining of Solanaceae sequences. The resource will allow the community access to the results of added-value comparative and de novo analyses on the partial genomic sequences provided by the Tomato, Potato and Tobacco genome sequencing efforts and Solanaceae transcript assemblies, and will link Solanaceae sequences to the functional genomics resources of other related model dicot species.</p>
  <h3>Project Goals</h3>
  <ul>
    <li>Provide up-to-date unified and integrated transcriptomic and genomic sequence data sets for the Solanaceae. </li>
    <li> Identify orthologous, paralogous and syntenic relationships among Solanaceae species. </li>
    <li>Link Solanaceae sequences to genomic data from model dicot species (Arabidopsis, Poplar, Grape) to leverage existing resources, especially functional genomics resources. </li>
    <li>Provide the Solanaceae community with a single access point for comparative genomics data and web services.</li>
    </ul>
